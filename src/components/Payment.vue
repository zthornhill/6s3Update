<template>
  <div>
    <div v-if="!paidFor">
      <br>
      <br>
      <h1>Donate! - ${{ product.price }}</h1>

      <p>{{ product.description }}</p>
      <img src="../assets/images/SubAlert.png">
    </div>

    <div v-if="paidFor">
      <h1>You have successfully donated to SubAlert!</h1>
    </div>

    <div ref="paypal"></div>
  </div>
</template>

<script>
export default {
  name: "Payment",
  data: function() {
    return {
      loaded: false,
      paidFor: false,
      product: {
        price: .99,
      }
    };
  },
  mounted: function() {
    const script = document.createElement("script");
    script.src =
      "https://www.paypal.com/sdk/js?client-id=Ae96A1SN06uFhpkhihBzvNos8HNSaqU41AIPUrKvGHz8uLWccud1IhpADdxDzmo4sBbziv6Mypx1YvMv";
    script.addEventListener("load", this.setLoaded);
    document.body.appendChild(script);
  },
  methods: {
    setLoaded: function() {
      this.loaded = true;
      window.paypal
        .Buttons({
          createOrder: (data, actions) => {
            return actions.order.create({
              purchase_units: [
                {
                  description: this.product.description,
                  amount: {
                    currency_code: "USD",
                    value: this.product.price
                  }
                }
              ]
            });
          },
          onApprove: async (data, actions) => {
            const order = await actions.order.capture();
            this.data;
            this.paidFor = true;
            console.log(order);
          },
          onError: err => {
            console.log(err);
          }
        })
        .render(this.$refs.paypal);
    }
  }
};
</script>

